function createCookie(name) {
	var expires = "";
	var prop3 = "";
	var pagetype = s.prop3;
	if(pagetype != null){
	var page = pagetype.split(":");
	if(page[0] == "category" || page[0] == "market" || page[0] == "application")
	{
	prop3 = page[0]+":"+"support"+":";
	}
	prop3 = prop3+"contact"+":";
	}
	if(document.dataForm.ftype){
	if(document.dataForm.ftype != ""){
	prop3 = prop3+document.dataForm.ftype.value+":";
	}
	}
	prop3 = prop3+"submit";
	var value = s.pageName+":"+"thankyou"+"*"+prop3;
	document.cookie = name+"="+value+expires+"; path=/";
	
}

function readCookie(name) {
	var nameEQ = name + "=";
	var ca = document.cookie.split(';');
	var result = "";
	for(var i=0;i < ca.length;i++) {
		var c = ca[i];
		while (c.charAt(0)==' ') c = c.substring(1,c.length);
		if (c.indexOf(nameEQ) == 0) {
		result = c.substring(nameEQ.length,c.length);

		break;
		}
	}
	if(result!= ""){
	var props = result.split("*");
	s.pageName = props[0];
	s.prop2 = s.pageName;
	s.channel = s.pageName;
	s.eVar1 = s.pageName;
	s.eVar2 = s.pageName;
	s.prop3 = props[1];
	
	}
	
	return null;
}

